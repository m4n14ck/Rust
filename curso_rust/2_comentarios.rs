fn main(){

    /* 
    Los comentarios son notas que escribes dentro de tu codigo.
    El compilador los ignora por completo; existen solo para
    ayudar a los humanos a entender codigo.

    Para escribir un comentario de una sola linea, usa //
    Todo lo que este despues de // hasta el final de la linea
    es ignrado.
    */
    
    // Este es un comentario
    println!("Hola mundo");

    // Un comentario tambien se puede escribir al final de una linea,
    // despues del codigo
    println!("Hola mundo"); // Esto imprime Hola mundo

    // Para comentarios que abarcan varias lineas, usa /* para
    // comenzar y */ para terminar 

    /* Este es un comentario de varias lineas.
       El compilador lo ignora todo */
    println!("Bienvenido");

    
}