fn main(){
    /*
    En Rust, existen varias convenciones de nomenclatura 
    que debes seguir para que tu código sea más legible y fácil de mantener. 
    Son ampliamente aceptadas por la comunidad de Rust.
    Aquí hay algunas convenciones de nomenclatura comunes:

    Las variables deben nombrarse usando snake_case, 
    lo que significa todas las letras en minúscula con guiones bajos entre palabras. 

    Por ejemplo: my_variable, calculate_total.

    Evita nombres excesivamente cortos o crípticos. 
    Elige nombres descriptivos que indiquen claramente el propósito de la variable.
    Los nombres pueden contener letras, dígitos y guiones bajos.
    Los nombres deben comenzar con una letra o un guion bajo _.

    Los contadores básicos suelen utilizar una sola letra como i, j, 
    aunque cabe señalar que los nombres más descriptivos podrían ser mejores cuando el contexto es más complejo.
     */

     let mi_variable = "hola";
     let mi_edad = 20;
     let mi_telefono = 2222222;

     println!("mi_variable = {}",mi_variable);
     println!("mi_edad = {}",mi_edad);
     println!("mi_telefono = {}",mi_telefono);

}