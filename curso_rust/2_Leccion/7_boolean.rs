fn main(){

    /* 
    Un tipo boolean tiene solo 2 valores posbles
    true o false.

    Para asignar un valor boolean a una variable,
    usa la palabra clave let seguida del nombre 
    de la variable.
     */

    let variable_true: bool = true;
    let variable_false: bool = false;

    println!("varibale true = {}",variable_true);
    println!("variable_false = {}",variable_false);

}